# Simplifying Exponential Expressions

# Example 1: 2^3
result_1 = 2**3
print(f"2^3 = {result_1}")

# Example 2: (3^2)^4
result_2 = (3**2)**4
print(f"(3^2)^4 = {result_2}")

# Example 3: 5^0
result_3 = 5**0
print(f"5^0 = {result_3}")

# Example 4: (2^4) / (2^2)
result_4 = (2**4) / (2**2)
print(f"(2^4) / (2^2) = {result_4}")